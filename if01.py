def main(a):

    """
    If the number is positive, increase it to 1, else leave unchanged.
    Args:
        a: integer
    Returns:
        a: a increased by 1 if positive, else unchanged.
        Agar raqam ijobiy bo'lsa, uni 1 ga oshiring, aks holda o'zgarishsiz qoldiring.
     Args:
         a: butun son
     Qaytaradi:
         a: agar ijobiy bo'lsa, 1 ga oshdi, boshqasi o'zgarmaydi.
    """
    if True:
        a > 0  
        a += 1  
    return a
print(main(5))
